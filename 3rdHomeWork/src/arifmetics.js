let num1 = 17; 
let num2 = 8;
let str1 = "100";
let str2 = "Flyers";

// Arithmetic operations on variables of the same type
console.log("Subtraction of numbers:", num1 - num2);
console.log("Multiplication of numbers:", num1 * num2);
console.log("String concatenation:", str1 + str2);

// Arithmetic operations on variables of different types
console.log("Addition of a number and a string:", num1 + str1);
console.log("Multiplication of a string-number by a number:", str1 * num2);
console.log("Subtraction of a string-number and a number:", str1 - num2);
